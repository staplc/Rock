      <h1>Local Attractions</h1>
      <p>The gentle beauty of  Exmoor and the drama of  it's coastline may be enough for many 

people. 
          There are many excellent walks from Lynmouth including Watersmeet and the Valley of 

Rocks in Lynton.The best way to the Valley is via the unique Victorian Cliff Railway.
          Or maybe you would like a Boat trip?
          There are boating and fishing trips from Lynmouth harbour or why not visit Lundy Island.
          Take a trip the historic fishing village of  Clovelly, the place that inspired Charles 

Kingsley to write the Water Babies.</p>
